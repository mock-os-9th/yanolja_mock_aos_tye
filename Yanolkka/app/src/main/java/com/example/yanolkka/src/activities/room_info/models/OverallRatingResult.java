public class OverallRatingResult {
    private double OverallRating;
    private int ReviewCount;
    private double KindnessRating;
    private double CleanlinessRating;
    private double ConvenienceRating;
    private double LocationRating;

    public double getOverallRating() {
        return OverallRating;
    }

    public int getReviewCount() {
        return ReviewCount;
    }

    public double getKindnessRating() {
        return KindnessRating;
    }

    public double getCleanlinessRating() {
        return CleanlinessRating;
    }

    public double getConvenienceRating() {
        return ConvenienceRating;
    }

    public double getLocationRating() {
        return LocationRating;
    }
}
